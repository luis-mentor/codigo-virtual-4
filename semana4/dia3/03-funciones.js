/**
    Una FUNCION es un conjunto de lineas de codigo, que se ejecutan
    cuando sean invocadas, no antes ni después

 */
function tablaDel5() {
    for (let c = 0; c <= 10; c++) {
        console.log(`${c} x 5 = ${c * 5}`);
    }
}
/** OBS:   Una función no se EJECUTA hasta que se la invoque*/
tablaDel5();
/* La ventaja de una FUNCION es que se puede llamar N veces */
console.log("---");
tablaDel5();
console.log("---");
tablaDel5();

